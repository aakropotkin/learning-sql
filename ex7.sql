--
-- 7.2
-- Write a query that returns the absolute value and sign ( -1, 0, or 1 ) of
-- the number -25.76823.
-- Also return the number rounded to the nearest hundredth.
--
-- -------------------------------------------------------------------------- --

SELECT
  ABS( -25.76823 ) AS absolute_value,
  CASE
    WHEN -25.76823 < 0 THEN -1
    WHEN -25.76823 = 0 THEN 0
    ELSE                    1
  END AS sign,
  ROUND( -25.76823, 2 ) as rounded
;

